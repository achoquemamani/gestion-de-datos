CREATE VIEW HARAKIRI.PRODUCTOS_CON_MAYOR_RENTABILIDAD_ANUAL
AS
SELECT TIEMPO_ANIO, PRODUCTO_NOMBRE, PORCENTAJE_RENTABILIDAD FROM (
SELECT T.TIEMPO_ANIO, P.PRODUCTO_NOMBRE, SUM(HVP.PRODUCTO_PRECIO_TOTAL) AS VENTA_TOTAL_PRODUCTO, SUM(HCP.PRODUCTO_CANTIDAD * HCP.PRODUCTO_PRECIO_UNITARIO) AS COMPRA_TOTAL_PRODUCTO, ((SUM(HVP.PRODUCTO_PRECIO_TOTAL) - SUM(HCP.PRODUCTO_CANTIDAD * HCP.PRODUCTO_PRECIO_UNITARIO))/SUM(HVP.PRODUCTO_PRECIO_TOTAL)) * 100 AS PORCENTAJE_RENTABILIDAD, ROW_NUMBER() OVER (PARTITION BY T.TIEMPO_ANIO ORDER BY ((SUM(HVP.PRODUCTO_PRECIO_TOTAL) - SUM(HCP.PRODUCTO_CANTIDAD * HCP.PRODUCTO_PRECIO_UNITARIO))/SUM(HVP.PRODUCTO_PRECIO_TOTAL)) * 100 DESC) AS TOP_MAYOR_RENTABILIDAD
FROM HARAKIRI.BI_HECHO_VENTA_PRODUCTO HVP
JOIN HARAKIRI.BI_PRODUCTO P ON P.PRODUCTO_CODIGO = HVP.PRODUCTO_CODIGO
JOIN HARAKIRI.BI_HECHO_COMPRA_PRODUCTO HCP ON HCP.PRODUCTO_CODIGO = P.PRODUCTO_CODIGO
JOIN HARAKIRI.BI_TIEMPO T ON T.TIEMPO_CODIGO = HVP.TIEMPO_CODIGO OR T.TIEMPO_CODIGO = HCP.TIEMPO_CODIGO
GROUP BY T.TIEMPO_ANIO, P.PRODUCTO_NOMBRE
) RENTABILIDADES
WHERE TOP_MAYOR_RENTABILIDAD <=5
GO

DECLARE @CANTIDAD_REGISTROS NVARCHAR(255)
SET @CANTIDAD_REGISTROS = (SELECT COUNT(*) FROM HARAKIRI.PRODUCTOS_CON_MAYOR_RENTABILIDAD_ANUAL)
PRINT('View PRODUCTOS_CON_MAYOR_RENTABILIDAD_ANUAL creada. Cant filas: ' + @CANTIDAD_REGISTROS)



